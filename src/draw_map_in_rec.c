int manage_events(sfRenderWindow *window, sfEvent event) {
    static int count = 0;

    while (sfRenderWindow_pollEvent(window, &event)) {
        if (event.type == sfEvtClosed) {
            sfRenderWindow_close(window);
            return 1;
        }
        if (event.type == sfEvtKeyPressed && event.key.code == sfKeyK) {
            count++;
        }
    }
    return count;
}

int **map_cop(int map[MAX_Y][MAX_X], int **tab) {
    for (int a = 0; a < MAX_Y; a++) {
        for (int b = 0; b < MAX_X; b++) {
            tab[a][b] = map[a][b];
        }
    }
}

sfRectangleShape *create(void)
{
    sfRectangleShape *rec = sfRectangleShape_create();

    sfRectangleShape_setSize(rec, (sfVector2f){30, 30});
    sfRectangleShape_setFillColor(rec, sfWhite);
    sfRectangleShape_setOutlineColor(rec, sfRed);
    sfRectangleShape_setOutlineThickness(rec, 1.5);
    return rec;
}

int loop(sfRenderWindow *window) {
    text_t *textures = init_textures();
    sfVector2f **positions = create_2d_map(50);
    sfRectangleShape *rec = create();
    sfEvent event;
    player_t player = {4, 0};
    int count = 0;

    int** tab = (int**)malloc(MAX_Y * sizeof(int*));
    for (int y = 0; y < MAX_Y; y++) {
        tab[y] = (int*)malloc(MAX_X * sizeof(int));
    }

    map_cop(map, tab);
    sfRenderWindow_setFramerateLimit(window, 60);
    while (sfRenderWindow_isOpen(window)) {
        count = manage_events(window, event);
        sfRenderWindow_clear(window, sfBlack);
        move(&player, rec);
        draw_2d_map(window, positions, textures, tab);
        sfRenderWindow_drawRectangleShape(window, rec, NULL);
        if (count % 2 != 0)
            inventaire_func(window);
        sfRenderWindow_display(window);
    }
    free_resources(textures, tab, positions);
    sfRectangleShape_destroy(rec);
    return 0;
}
