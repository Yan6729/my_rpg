sfVertexArray *create_quad(sfVector2f point[4], sfTexture *texture) {
    sfVertexArray *vertex_array = sfVertexArray_create();
    sfVector2u tex_size = sfTexture_getSize(texture);
    sfVertex vertex[4] = {
        {.position = point[0], .texCoords = (sfVector2f){4, 0}},
        {.position = point[1], .texCoords = (sfVector2f){tex_size.x, 0}},
        {.position = point[2], .texCoords = (sfVector2f){tex_size.x, tex_size.y}},
        {.position = point[3], .texCoords = (sfVector2f){4, tex_size.y}},
    };

    sfVertexArray_setPrimitiveType(vertex_array, sfQuads);
    for (int i = 0; i < 4; i++) {
        vertex[i].color = sfWhite;
        sfVertexArray_append(vertex_array, vertex[i]);
    }
    return vertex_array;
}

sfVector2f **create_2d_map(int zoom) {
    sfVector2f **vector = malloc(sizeof(sfVector2f *) * MAX_Y);
    
    for (int a = 0; a < MAX_Y; a++) {
        vector[a] = malloc(sizeof(sfVector2f) * MAX_X);
        for (int b = 0; b < MAX_X; b++) {
            vector[a][b] = (sfVector2f){(float)(b * zoom), (float)(a * zoom)};
        }
    }
    return vector;
}

void move(player_t *player, sfRectangleShape *rec)
{
    int new_x = player->x;
    int new_y = player->y;

    if (sfKeyboard_isKeyPressed(sfKeyZ)) {
        new_y -= 3;
        if (new_y > 0)
            player->y = new_y;
    }
    if (sfKeyboard_isKeyPressed(sfKeyS)) {
        new_y += 3;
        if (new_y < (1080 - 30))
            player->y =	new_y;
    }
    if (sfKeyboard_isKeyPressed(sfKeyQ)) {
        new_x -= 3;
        if (new_x > 0)
            player->x =	new_x;
    }
    if (sfKeyboard_isKeyPressed(sfKeyD)) {
        new_x += 3;
        if (new_x < (1920 - 30))
            player->x =	new_x;
    }
    sfRectangleShape_setPosition(rec, (sfVector2f){player->x, player->y});
}

void draw_2d_map(sfRenderWindow *window, sfVector2f **two_d_map, text_t *textures, int **map) {
    sfVector2f point[4];
    sfVertexArray *arr;
    sfRenderStates states;

    for (int j = 0; j < MAX_Y - 1; j++) {
        for (int i = 0; i < MAX_X - 1; i++) {
            point[0] = two_d_map[j][i];
            point[1] = two_d_map[j][i + 1];
            point[2] = two_d_map[j + 1][i + 1];
            point[3] = two_d_map[j + 1][i];
            sfTexture *current_texture = NULL;
            if (map[j][i] == 0) {
                current_texture = textures->tex_sol;
            } else if (map[j][i] == 1) {
                current_texture = textures->tex_herbe;
            } else if (map[j][i] == 4) {
                current_texture = textures->tex_arbre;
            } else if (map[j][i] == 3) {
                current_texture = textures->tex_case;
            } else if (map[j][i] == 2) {
                current_texture = textures->tex_barr;
            }  else if (map[j][i] == 5) {
                current_texture = textures->tex_barr_;
            } else {
                current_texture = textures->tex_case;
            }
            arr = create_quad(point, current_texture);
            states = (sfRenderStates){
                .texture = current_texture,
                .blendMode = sfBlendAlpha,
                .transform = sfTransform_Identity,
                .shader = NULL
            };
            sfRenderWindow_drawVertexArray(window, arr, &states);
            sfVertexArray_destroy(arr);
        }
    }
}

